function generateIconData(count) {
  const icons = [];
  const usedHashes = new Set();
  const totalTemplates = iconTemplates.length;
  
  // ÂêÑÁ®Æ„Éê„É™„Ç®„Éº„Ç∑„Éß„É≥Ë®≠ÂÆö
  const fillOptions = ['currentColor', 'none', '#000', '#333', '#666'];
  const strokeOptions = ['none', 'currentColor', '#000', '#666'];
  const strokeWidthOptions = ['0', '0.5', '1', '1.5', '2'];
  const opacityOptions = ['1', '0.9', '0.8', '0.7'];
  
  let attempted = 0;
  let index = 0;
  
  while (icons.length < count && attempted < count * 2) {
    const template = iconTemplates[index % totalTemplates];
    const category = categories[index % categories.length];
    const adjective = adjectives[index % adjectives.length];
    const variation = Math.floor(index / totalTemplates) + 1;
    
    // „Éê„É™„Ç®„Éº„Ç∑„Éß„É≥Ë®≠ÂÆö„ÇíË®àÁÆó
    const fillIndex = Math.floor(index / totalTemplates) % fillOptions.length;
    const strokeIndex = Math.floor(index / (totalTemplates * fillOptions.length)) % strokeOptions.length;
    const strokeWidthIndex = Math.floor(index / (totalTemplates * fillOptions.length * strokeOptions.length)) % strokeWidthOptions.length;
    const opacityIndex = Math.floor(index / (totalTemplates * fillOptions.length * strokeOptions.length * strokeWidthOptions.length)) % opacityOptions.length;
    const transformIndex = Math.floor(index / (totalTemplates * fillOptions.length * strokeOptions.length * strokeWidthOptions.length * opacityOptions.length));
    
    const fill = fillOptions[fillIndex];
    const stroke = strokeOptions[strokeIndex];
    const strokeWidth = strokeWidthOptions[strokeWidthIndex];
    const opacity = opacityOptions[opacityIndex];
    const transform = applyTransform(template.path, transformIndex);
    
    // SVGÂ±ûÊÄß„ÇíÊßãÁØâ
    let svgAttrs = `viewBox="0 0 24 24" xmlns="http://www.w3.org/2000/svg"`;
    let pathAttrs = `d="${template.path}"`;
    
    if (fill) pathAttrs += ` fill="${fill}"`;
    if (stroke !== 'none') {
      pathAttrs += ` stroke="${stroke}"`;
      if (strokeWidth !== '0') pathAttrs += ` stroke-width="${strokeWidth}"`;
    }
    if (opacity !== '1') pathAttrs += ` opacity="${opacity}"`;
    if (transform) pathAttrs += ` ${transform}`;
    
    // „É¶„Éã„Éº„ÇØID„ÇíËøΩÂä†
    pathAttrs += ` id="p${index}"`;
    
    const svg = `<svg ${svgAttrs}><path ${pathAttrs}/></svg>`;
    const hash = generateHash(svg);
    
    // „Éè„ÉÉ„Ç∑„É•ÈáçË§á„ÉÅ„Çß„ÉÉ„ÇØ
    if (!usedHashes.has(hash)) {
      usedHashes.add(hash);
      
      const id = `icon-${String(icons.length + 1).padStart(5, '0')}`;
      const title = variation > 1 ? `${adjective} ${template.type} ${variation}` : `${adjective} ${template.type}`;
      const description = `${category}„Ç´„ÉÜ„Ç¥„É™„ÅÆ${template.type}„Ç¢„Ç§„Ç≥„É≥Ôºà${adjective}„Çπ„Çø„Ç§„É´${variation > 1 ? `„ÄÅ„Éê„É™„Ç®„Éº„Ç∑„Éß„É≥${variation}` : ''}Ôºâ„ÄÇ${getDescriptionText(template.type, category)}`;
      
      const tags = [template.type.split('-')[0], category.toLowerCase(), adjective, 'icon'];
      
      icons.push({
        id,
        title,
        description,
        svg,
        hash,
        tags,
        category
      });
    }
    
    index++;
    attempted++;
  }
  
  return icons;
}

